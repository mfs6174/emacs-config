	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX
	MOV AX,BX
	MOV BX,10
	XOR CX,CX
	XOR DX,DX
PRINT_D_L1:
	DIV BX
	PUSH DX
	INC CX
	XOR DX,DX
	CMP AX,0
	JNZ PRINT_D_L1
	MOV AH,02H
PRINT_D_L2:
	POP DX
	ADD DL,48
	INT 21H
	LOOP PRINT_D_L2
	MOV DL,' '
	INT 21H
	POP DX
	POP CX
	POX BX
	POP AX
	RET
PRINT_D ENDP
